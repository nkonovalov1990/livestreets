// Функция для нормализации имени директории
function normalizeDirectoryName(dirName) {
    // Заменяем пробелы и специальные символы на дефисы, удаляем русские буквы
    return dirName
        .replace(/[\s\/\\]/g, '-') // заменяем пробелы и слеши на дефисы
        .replace(/[а-яА-ЯёЁ]/g, '') // удаляем русские буквы
        .replace(/-+/g, '-') // заменяем множественные дефисы на один
        .replace(/^-|-$/g, '') // удаляем дефисы в начале и конце
        .toLowerCase();
}

